class AS_10{
    public static void main(String args[]){
        short value=5000;
        Short bs=new Short(value);
        byte bs1=bs.byteValue();
        System.out.println(bs1);

        Short bs2=bs.shortValue();
        System.out.println(bs2);

        int bs3=bs.intValue();
        System.out.println(bs3);

        long bs4=bs.longValue();
        System.out.println(bs4);

        float bs5=bs.floatValue();
        System.out.println(bs5);

        double bs6=bs.doubleValue();
        System.out.println(bs6);


        






    }
}