class Gross_salary{
	public static void main(String args[])
	{
		
	double basic_salary =10000.8989008;
	double TA =5600.677;
	double DA =0.14*basic_salary;//(14*basic salary)/100=DA
	double PA =7000.3443534546;
	double TD =0.017*DA;//(1.7*DA)/100=TD
	double Gross_salary;
	System.out.println("basic_salary = "+basic_salary);
	System.out.println("TA = "+Ta);
	System.out.println("Total salary = " +Gross_salary);
	Gross_salary=basic_salary+TA+DA+PA-TD;
	
	System.out.println("Total salary = " +Gross_salary);
	
	
	}
}